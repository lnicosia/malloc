int	is_a_special_char(char c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9') || c == '_' || c == '.'
		|| c == '-' || c == '+' || c == '{' || c == '}'
		|| c == '~' || c == ':' || c == '#' || c == '%'
		|| c == ',' || c == '@' || c == '/')
		return (0);
	if (c == '\'')
		return (2);
	if (c == '\n')
		return (3);
	return (1);
}